
// while loop in js 

let i=0

while (i<=10) {
    console.log(`value of index is ${i}`);
    i=i+2
    
}

// array in while loop

let myarr =["flash", "batman", "superman"] // in this first the condition is checked 

let arr=0

while (arr<myarr.length) {

    console.log(`value is ${myarr[arr]}`);
    arr++;
    
    
}

